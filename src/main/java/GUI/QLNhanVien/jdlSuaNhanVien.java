    private void LoadWhenOpen() {
        ArrayList<NhanVien> arrdd = BLL.BLLNhanVien.GetAllNhanVienbyMa(manhanvien);
        for (NhanVien nv : arrdd) {
            txttennv.setText(nv.getTenNhanvien());
            txtsdt.setText((nv.getSoDienThoai()));
            txtemail.setText((nv.getEmail()));
            if (nv.isGioiTinh()) {
                cbnam.setSelected(true);
                cbnu.setSelected(false);
            } else {
                cbnam.setSelected(false);
                cbnu.setSelected(true);
            }
            if (nv.isTinhTrang()) {
                cbyes.setSelected(true);
                cbno.setSelected(false);
            } else {
                cbyes.setSelected(false);
                cbno.setSelected(true);
            }

            photo = nv.getHinhAnh();
            ImageIcon imageIcon = new ImageIcon(new ImageIcon(photo).getImage().
                    getScaledInstance(lbimage.getWidth(), lbimage.getHeight(), Image.SCALE_DEFAULT));
            lbimage.setIcon(imageIcon);
            txtluong.setText(Transform.SoString(nv.getLuong()));
            txttendangnhap.setText(nv.getTenDangNhap());
            txtmatkhau.setText(nv.getMatKhau());
            datengaysinh.setDate(nv.getNgaySinh());
            txtdiachi.setText(nv.getDiaChi());
        }
        BLL.BLLKhac.LoadCBBKhiMo(cbchucvu, BLL.BLLKhac.loadTenChucVu(arrdd.get(0).getChucVu()));
    }

