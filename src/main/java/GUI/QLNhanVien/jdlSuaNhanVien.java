    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        if (txttennv.getText().equals("")) {
            Caution.ThongBaoDonGian("Thông báo", "Tên không được để trống!");
        } else if (datengaysinh.getDate() == null) {
            Caution.ThongBaoDonGian("Thông báo", "Ngày sinh không được để trống!");
        } else if (!cbnam.isSelected() && !cbnu.isSelected()) {
            Caution.ThongBaoDonGian("Thông báo", "Vui lòng chọn giới tính!");
        } else if (txtsdt.getText().equals("")) {
            Caution.ThongBaoDonGian("Thông báo", "Số điện thoại không được để trống!");
        } else if (txtemail.getText().equals("")) {
            Caution.ThongBaoDonGian("Thông báo", "Email không được để trống!");
        } else if (txtdiachi.getText().equals("")) {
            Caution.ThongBaoDonGian("Thông báo", "Địa chỉ không được để trống!");
        } else if (txtluong.getText().equals("")) {
            Caution.ThongBaoDonGian("Thông báo", "Lương không được để trống!");
        } else if (txttendangnhap.getText().equals("")) {
            Caution.ThongBaoDonGian("Thông báo", "Tên đăng nhập không được để trống!");
        } else if (txtmatkhau.getText().equals("")) {
            Caution.ThongBaoDonGian("Thông báo", "Mật khẩu không được để trống!");
        } else if (photo == null) {
            Caution.ThongBaoDonGian("Thông báo", "Vui lòng chọn hình ảnh!");
        } else if (txtsdt.getText().length() != 10) {
            Caution.ThongBaoDonGian("Thông báo", "Số điện thoại chỉ được 10 số!");
        } else {
            String tennv = txttennv.getText();
            String sdt = txtsdt.getText();
            String email = txtemail.getText();
            String diachi = txtdiachi.getText();
            Double luong = Transform.SoDouble(txtluong.getText());
            String tendn = txttendangnhap.getText();
            String mk = txtmatkhau.getText();
            byte[] img = photo;
            boolean tinhtrang = cbyes.isSelected() ? true : false;
            boolean gioitinh = cbnam.isSelected() ? true : false;
            Date ngaysinh = datengaysinh.getDate();
            Date date = new Date();
            SimpleDateFormat sim = new SimpleDateFormat("yyyy");
            int namsinh = Integer.parseInt(sim.format(ngaysinh));
            int namhientai = Integer.parseInt(sim.format(date));
            if ((namhientai - namsinh < 16)) {
                Caution.ThongBaoDonGian("Thông báo", "Nhân viên " + tennv + " chưa đủ 16 tuổi");
            } else {
                MyComboBox cBB = (MyComboBox) cbchucvu.getSelectedItem();
                int chucvu = cBB.MaInt();

                ArrayList<NhanVien> arr = BLL.BLLNhanVien.GetAllNhanVienbyMa(manhanvien);
                for (NhanVien nv : arr) {
                    if (nv.getMaNhanVien() == (manhanvien) && nv.getSoDienThoai().equals(sdt) && nv.getEmail().equals(email) && nv.getTenDangNhap().equals(tendn)) {
                        NhanVien nvv = new NhanVien(tennv, ngaysinh, chucvu, sdt, email, diachi, gioitinh, luong, tendn, mk, img, tinhtrang);
                        BLL.BLLNhanVien.update(nvv, manhanvien, email);
                        loadnhanvien();
                    } else if (!nv.getSoDienThoai().equals(sdt)) {
                        NhanVien nvv = new NhanVien(tennv, ngaysinh, chucvu, sdt, email, diachi, gioitinh, luong, tendn, mk, img, tinhtrang);
                        BLL.BLLNhanVien.updatechecksdt(nvv, manhanvien, email);
                        loadnhanvien();
                    } else if (!nv.getEmail().equals(email)) {
                        NhanVien nvv = new NhanVien(tennv, ngaysinh, chucvu, sdt, email, diachi, gioitinh, luong, tendn, mk, img, tinhtrang);
                        BLL.BLLNhanVien.updatecheckemail(nvv, manhanvien, email);
                        loadnhanvien();
                    } else if (!nv.getTenDangNhap().equals(tendn)) {
                        NhanVien nvv = new NhanVien(tennv, ngaysinh, chucvu, sdt, email, diachi, gioitinh, luong, tendn, mk, img, tinhtrang);
                        BLL.BLLNhanVien.updatechecktendn(nvv, manhanvien, email);
                        loadnhanvien();
                    }else{
                        NhanVien nvv = new NhanVien(tennv, ngaysinh, chucvu, sdt, email, diachi, gioitinh, luong, tendn, mk, img, tinhtrang);
                        BLL.BLLNhanVien.updatecheck(nvv, manhanvien, email);
                        loadnhanvien();
                    }
                }

            }
        }
    }//GEN-LAST:event_btnluuActionPerformed

