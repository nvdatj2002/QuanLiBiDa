    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        loadkhuvuc();
        loadloaiban();
        loadHistory();
        ArrayList<Ban> arrTable = BLL.BLLBan.listTablesByName(lbtxtName.getText());
        txttenchitietban.setText(arrTable.get(0).getTenBan());
        BLL.BLLBan.LoadLBAndKV(cbbkhuvuc, BLL.BLLKhac.GetNameKhuVuc(arrTable.get(0).getKhuVuc()));
        BLL.BLLBan.LoadLBAndKV(cbbloaiban, BLL.BLLKhac.GetNameLoaiBan(arrTable.get(0).getLoaiBan()));
        if (arrTable.get(0).getTinhTrang().equals("0")) {
            checkct.setSelected(true);
            checkdc.setSelected(false);
            checkdd.setSelected(false);
        } else if (arrTable.get(0).getTinhTrang().equals("1")) {
            checkct.setSelected(false);
            checkdc.setSelected(true);
            checkdd.setSelected(false);
        } else {
            checkct.setSelected(false);
            checkdc.setSelected(false);
            checkdd.setSelected(true);
        }
    }//GEN-LAST:event_formWindowOpened

