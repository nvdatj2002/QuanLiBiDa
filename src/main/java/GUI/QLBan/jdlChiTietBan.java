    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        MyComboBox cbbkv = (MyComboBox) cbbkhuvuc.getSelectedItem();
        MyComboBox cbblb = (MyComboBox) cbbloaiban.getSelectedItem();
        String checktinhtrang = "";
        if (checkct.isSelected()) {
            checktinhtrang = "0";
        } else if (checkdc.isSelected()) {
            checktinhtrang = "1";
        } else {
            checktinhtrang = "2";
        }
        Ban ban = new Ban(txttenchitietban.getText(), cbblb.MaInt(), cbbkv.MaInt(), checktinhtrang);
        BLL.BLLBan.SuaSanpham(ban);
        dispose();
    }//GEN-LAST:event_btnsuaActionPerformed

