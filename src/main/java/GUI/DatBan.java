        txtNgay.setDate(null);
        txtSoDienThoai.setText("");
        txtTenKhach.setText("");
        jpnBanDangTrong.removeAll();
        jpnBanDaDat.removeAll();

        MyComboBox cbbKhuVucDaDat = (MyComboBox) CBBKhuVucBanDangTrong.getSelectedItem();
        if (CBBLoaiBanDangTrong == null) {
            return;
        }
        int maKhuVucDaDat = cbbKhuVucDaDat.MaInt();
        if (!loadCBBLoaiaBanOK) {
            return;
        }
        MyComboBox cbbLoaiBanDatDat = (MyComboBox) CBBLoaiBanDangTrong.getSelectedItem();
        if (cbbLoaiBanDatDat == null) {
            return;
        }
        int maLoaiBanDatDat = cbbLoaiBanDatDat.MaInt();
        ArrayList<Ban> listDaDat = BLLBan.timBanTheoMaLoaiVaKhuVuc(maKhuVucDaDat, maLoaiBanDatDat);

        MyComboBox cbbKhuVuc = (MyComboBox) CBBKhuVucBanDaDat.getSelectedItem();
