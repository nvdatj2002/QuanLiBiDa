        int maBanCu = arrBanCu.get(0).getMaBan();
        DTO.DatBan ban = new DTO.DatBan();
        ban.setMaBan(maBanCu);
        ban.setMaNhanVien(BLL.BLLNhanVien.GetAccountNhanVien().getMaNhanVien());
        ban.setNgayDat(new Date());
        ban.setNgayChoi(date);
        ban.setSoDienThoai(soDienThoai);
        ban.setTenKhach(tenKhach);
        ban.setGhiChu(ghiChu);
        ban.setTinhTrang("1");
        BLLDatBan.insert(ban);
        
