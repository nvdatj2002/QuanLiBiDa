    public static void LoadKhac() {
        if (TenKhacTuCBX.equals("Khuyến mãi")) {
            ArrayList<KhuyenMai> arr = BLL.BLLKhac.GetAllKhuyenMai();
            BLL.BLLKhac.DoVaoTableKhuyenMai(arr, tbchung);
        }
        if (TenKhacTuCBX.equals("Loại đồ dùng")) {
            ArrayList<LoaiDoDung> arr = BLL.BLLKhac.GetAllLoaiDoDung();
            BLL.BLLKhac.DoVaoTableLoaiDoDung(arr, tbchung);
        }
        if (TenKhacTuCBX.equals("Chức vụ")) {
            ArrayList<ChucVu> arr = BLL.BLLKhac.GetAllChucVu();
            BLL.BLLKhac.DoVaoTableChucVu(arr, tbchung);
        }
        if (TenKhacTuCBX.equals("Khu vực")) {
            ArrayList<KhuVuc> arr = BLL.BLLKhac.GetAllKhuVuc();
            BLL.BLLKhac.DoVaoTableKhuVuc(arr, tbchung);
        }
        if (TenKhacTuCBX.equals("Đơn vị tính")) {
            ArrayList<DonViTinh> arr = BLL.BLLKhac.GetAllDVT();
            BLL.BLLKhac.DoVaoTableDVT(arr, tbchung);
        }
        if (TenKhacTuCBX.equals("Loại bàn")) {
            ArrayList<LoaiBan> arr = BLL.BLLKhac.GetAllLoaiBan();
            BLL.BLLKhac.DoVaoTableLoaiBan(arr, tbchung);
        }
    }

