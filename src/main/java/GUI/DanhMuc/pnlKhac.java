    private void cbloaidodungItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbloaidodungItemStateChanged
        TenKhacTuCBX = cbloaidodung.getSelectedItem().toString();
        LoadKhac();
        TenKhacTuTable = "";
        if (cbloaidodung.getSelectedItem().toString().equals("Loại bàn")) {
            jdlthemloaiban jdl = new jdlthemloaiban(new HomeScreen(), true);
            pnlbtncon(a, jdl, btnthem, btnxuatds, btndelete);
        } else if (cbloaidodung.getSelectedItem().toString().equals("Khuyến mãi")) {
            jdlThemKhuyenMai km = new jdlThemKhuyenMai(new HomeScreen(), true);
            pnlbtncon(a, km, btnthem, btnxuatds, btndelete);
        } else {
            jdlthemkhac khac = new jdlthemkhac(new HomeScreen(), true);
            pnlbtncon(a, khac, btnthem, btnxuatds, btndelete);
        }


    }//GEN-LAST:event_cbloaidodungItemStateChanged

    private void tbchungMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbchungMousePressed
        index = tbchung.getSelectedRow();
        TenKhacTuTable = tbchung.getValueAt(index, 1).toString();
        if (index >= 0 && evt.getClickCount() > 1) {
            TenKhacTuTable = tbchung.getValueAt(index, 1).toString();
            if (cbloaidodung.getSelectedItem().toString().equals("Loại bàn")) {
                GiatienTuTable = tbchung.getValueAt(index, 2).toString();
                jdlsualoaiban jdlslb = new jdlsualoaiban(new HomeScreen(), true);
                jdlslb.setVisible(true);
            } else if (cbloaidodung.getSelectedItem().toString().equals("Khuyến mãi")) {
                MaKhuyenMaiTuTable = Integer.parseInt(tbchung.getValueAt(index, 1).toString());
                jdlSuaKhuyenMai jdlslb = new jdlSuaKhuyenMai(new HomeScreen(), true);
                jdlslb.setVisible(true);
            } else {
                jdlSuaKhac jdlsuadd = new jdlSuaKhac(new HomeScreen(), true);
                jdlsuadd.setVisible(true);

            }
        }
    }//GEN-LAST:event_tbchungMousePressed


