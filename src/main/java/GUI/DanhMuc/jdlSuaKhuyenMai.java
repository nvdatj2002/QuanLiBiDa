    public void LoadWhenOpen() {
        ArrayList<KhuyenMai> arr = BLL.BLLKhac.GetAllKhuyenMaibyMa(MaKhuyenMaiTuTable);
        for (KhuyenMai km : arr) {
            txtten.setText(km.getTenKhuyenMai());
            String abc = String.valueOf(km.getGiaTri());
            txtgiatri.setText(abc);
            if (km.isTinhTrang()) {
                cbtrue.setSelected(true);
                cbfalse.setSelected(false);
            } else {
                cbtrue.setSelected(false);
                cbfalse.setSelected(true);
            }
        }
    }

