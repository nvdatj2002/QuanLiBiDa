    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        if (txtten.getText().isEmpty()) {
            Caution.ThongBaoDonGian("Thông báo", "Tên không được bỏ trống");
        } else if (txtgiatri.getText().isEmpty()) {
            Caution.ThongBaoDonGian("Thông báo", "Giá trị không được bỏ trống");
        } else if (!cbtrue.isSelected() && !cbfalse.isSelected()) {
            Caution.ThongBaoDonGian("Thông báo", "Vui lòng chọn tình trạng!");
        } else {
            boolean tinhtrang = cbtrue.isSelected() ? true : false;
            KhuyenMai km = new KhuyenMai(txtten.getText(), Integer.parseInt(txtgiatri.getText()), tinhtrang);
            BLL.BLLKhac.SuaKhuyenMai(km, MaKhuyenMaiTuTable);
            pnlKhac.LoadKhac();
        }

    }//GEN-LAST:event_btnluuActionPerformed

