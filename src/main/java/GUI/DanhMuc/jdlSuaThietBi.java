    public void LoadTBWhenOpen() {
        ArrayList<ThietBi> arrdd = BLL.BLLThietBi.TimKiemThietBiBangMa(MaThietBi);
        for (ThietBi tb : arrdd) {
            txtten.setText(tb.getTenThietBi());
            txtgianhap.setText(Transform.SoString(tb.getGiaNhap()));
            spnsoluong.setValue(tb.getSoLuong());
            photo = tb.getHinhAnh();
            ImageIcon imageIcon = new ImageIcon(new ImageIcon(photo).getImage().
                    getScaledInstance(lbimage.getWidth(), lbimage.getHeight(), Image.SCALE_DEFAULT));
            lbimage.setIcon(imageIcon);
        }
        BLL.BLLKhac.LoadCBBKhiMo(cbdvttb, BLL.BLLKhac.loadTenDVT(arrdd.get(0).getDVT()));

    }

