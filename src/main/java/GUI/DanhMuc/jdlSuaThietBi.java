    private void btnhinhanhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhinhanhActionPerformed
        try {
            JFileChooser fileChooser = new JFileChooser();
            int trangthai = fileChooser.showOpenDialog(null);
            if (trangthai == JFileChooser.APPROVE_OPTION) {
                File file = fileChooser.getSelectedFile();
                ImageIcon imageIcon = new ImageIcon(new ImageIcon(file.toString()).getImage().getScaledInstance(
                        lbimage.getWidth(), lbimage.getHeight(), Image.SCALE_DEFAULT)
                );
                lbimage.setIcon(imageIcon);
                FileInputStream fis = new FileInputStream(file);
                ByteArrayOutputStream bos = new ByteArrayOutputStream();
                byte[] buf = new byte[1024];
                for (int readnum; (readnum = fis.read(buf)) != -1;) {
                    bos.write(buf, 0, readnum);
                }
                photo = bos.toByteArray();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnhinhanhActionPerformed

