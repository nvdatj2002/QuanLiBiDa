    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        if (txtten.getText().equals("")) {
            Caution.ThongBaoDonGian("Thông báo", "Tên không được để trống!");
        } else if (txtgianhap.getText().equals("")) {
            Caution.ThongBaoDonGian("Thông báo", "Giá nhập không được để trống!");
        } else if (photo == null) {
            Caution.ThongBaoDonGian("Thông báo", "Hình ảnh không được để trống!");
        } else if (Integer.parseInt(spnsoluong.getValue().toString()) <= 0) {
            Caution.ThongBaoDonGian("Thông báo", "Số lượng phải lớn hơn 0!");
        } else {
            String Ten = txtten.getText();
            Double gianhap = Transform.SoDouble(txtgianhap.getText());
            int soluong = (int) spnsoluong.getValue();
            MyComboBox cBBB = (MyComboBox) cbdvttb.getSelectedItem();
            int DVT = cBBB.MaInt();
            byte[] img = photo;
            ThietBi dd = new ThietBi(Ten, gianhap, soluong, DVT, img);
            BLL.BLLThietBi.SuaThietBi(dd, MaThietBi);
            pnlThietBi.LoadThietBi();
        }

    }//GEN-LAST:event_btnluuActionPerformed

